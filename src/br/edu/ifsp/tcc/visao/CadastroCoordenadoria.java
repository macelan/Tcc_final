       private void trataEdicao(boolean editando) {
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnAlterar.setEnabled(!editando);
        btnNovo.setEnabled(!editando);
        txtNome.setEditable(editando);
        txtSigla.setEditable(editando);
        //txtLocalizarCoordenadoria.setEditable(editando);
        jTableCoordenadoria.setEnabled(!editando);
  

    }

    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jTableCoordenadoria.getModel(); // manipulador de tabela DefaultTableModel
        dtm.setNumRows(0);//apagando todas as linhas / o zero serva para apagar as linhas "é muito rapido"
        for (Coordenadoria f : listaCoordenadorias) {
            dtm.addRow(new Object[]{f.getSigla(), f.getNome()}); // Object é um vetor ou coleção com ele não pecisa converter
        }
    }

    
    public void limparCampos() {
        txtNome.setText("");
        txtSigla.setText("");
        txtLocalizarCoordenadoria.setText("");
    }
    
    public void carregarLista() {
        listaCoordenadorias = controleCoordenadoria.buscar();
    }
    
    public void pesquisar() {
        listaCoordenadorias.clear();//limpa lista
        DefaultTableModel dtm = (DefaultTableModel) jTableCoordenadoria.getModel();
        dtm.setNumRows(0);//LIMPA A TABELA
        String str = "";
        str = txtLocalizarCoordenadoria.getText();
        if (jcbPesquisarCoordenadoria.getSelectedItem().toString().equals("Sigla")) {
            listaCoordenadorias.addAll(controleCoordenadoria.listarTurno(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        }
        if (jcbPesquisarCoordenadoria.getSelectedItem().toString().equals("Nome")) {
            listaCoordenadorias.addAll(controleCoordenadoria.listarNome(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        }
        txtLocalizarCoordenadoria.requestFocus();
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }
    
    public void TamanhoColuna() {
        jTableCoordenadoria.getColumnModel().getColumn(0).setMaxWidth(350);
        jTableCoordenadoria.getColumnModel().getColumn(1).setMaxWidth(700);
        jTableCoordenadoria.setAutoCreateRowSorter(true); // ordena ao clicar na coluna
        JTableHeader jth = jTableCoordenadoria.getTableHeader();
        jth.setFont(new Font("Arial Unicod MS", Font.BOLD, 14));
    }

    public void vincularCampos() {
        Coordenadoria b = listaCoordenadorias.get(jTableCoordenadoria.getSelectedRow());// pega a linha da tabela
        txtNome.setText(b.getNome());
        txtSigla.setText(b.getSigla());
      
    }


      

