    public void pesquisar() {
        listaPresidios.clear();//limpa lista
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqPresidio.getModel();
        dtm.setNumRows(0);//LIMPA A TABELA
        String str = "";
        str = txtLocalizar.getText();
        if (jcbPesquisarPresidio.getSelectedItem().toString().equals("Nome")) {
            listaPresidios.addAll(ControlePresidio.listarNome(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
            jcbPesquisarPresidio.requestFocus();
        }
        if (jcbPesquisarPresidio.getSelectedItem().toString().equals("Sigla Presidio")) {
            listaPresidios.addAll(ControlePresidio.listarSigla(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
            jcbPesquisarPresidio.requestFocus();
        }
        if (jcbPesquisarPresidio.getSelectedItem().toString().equals("Sigla Coordenadoria")) {
            listaPresidios.addAll(ControlePresidio.listarCoordenadoria(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
            jcbPesquisarPresidio.requestFocus();
        }
        txtLocalizar.requestFocus();
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS

    }


