    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jtbPesqPresidio.getModel(); // manipulador de tabela DefaultTableModel
        dtm.setNumRows(0);//apagando todas as linhas / o zero serva para apagar as linhas "é muito rapido"
        for (Presidio f : listaPresidios) {
            dtm.addRow(new Object[]{f.getNome(), f.getSigla(), f.getCoordenadoria().getSigla()}); // Object é um vetor ou coleção com ele não pecisa converter
        }
    }

