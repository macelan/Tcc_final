    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (jDateChooserDataObservacao.getDate() != null) {
            if (!txtAdicionaMatriculaSentenciado.getText().equals("")) {
                if (!txtRelato.getText().isEmpty()) {
                    f.setDataOcorrido(jDateChooserDataObservacao.getDate());
                    f.setDataRegistro(new Date());
                    f.setRelato(txtRelato.getText().trim());
                    f.setSentenciado(sentenciado);
                    controle.alterar(f);//salvar o orçamento
                    carregarLista();
                    atualizaTabela();
                    limparCampos();
                    trataEdicao(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Relate o Ocorrido!", "Alerta", JOptionPane.WARNING_MESSAGE);
                    txtRelato.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione o Sentenciado!", "Alerta", JOptionPane.WARNING_MESSAGE);
                txtAdicionaMatriculaSentenciado.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione a data da Observação!", "Alerta", JOptionPane.WARNING_MESSAGE);
            jDateChooserDataObservacao.requestFocus();
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (jTableObservacao.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma linha da tabela para excluir", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            if (JOptionPane.showConfirmDialog(null, "Confirma exclusão?",
                    "Exclusão da Observação", JOptionPane.YES_NO_OPTION)
                    == JOptionPane.YES_OPTION) {
                Observacao f = listObservacao.get(jTableObservacao.getSelectedRow());
                controle.remover(f);
            }
        }
        carregarLista();
        atualizaTabela();
        limparCampos();

    }//GEN-LAST:event_btnExcluirActionPerformed

