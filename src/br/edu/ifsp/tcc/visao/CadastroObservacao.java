    public void atualizaTabela() {
        DefaultTableModel dtm = (DefaultTableModel) jTableObservacao.getModel();
        dtm.setNumRows(0);//apagando todas as linhas
        for (Observacao f : listObservacao) {
            dtm.addRow(new Object[]{f.getSentenciado().getMatricula(), df.format(f.getDataRegistro()), dfh.format(f.getDataOcorrido()), f.getRelato()});
        }
    }

    public void carregarLista() {
        listObservacao = controle.buscar();
    }

    public void limparCampos() {
        jDateChooserDataObservacao.setDate(null);
        txtAdicionaMatriculaSentenciado.setText("");
        txtRelato.setText("");

    }

    public void pesquisar() {
        listObservacao.clear();//limpa lista
        DefaultTableModel dtm = (DefaultTableModel) jTableObservacao.getModel();
        dtm.setNumRows(0);//LIMPA A TABELA
        String str = "";
        str = txtLocalizarObservacao.getText();
        if (jcbPesquisarEventoColetivo.getSelectedItem().toString().equals("Matricula")) {
            listObservacao.addAll(controle.listarMatricula(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        }
        if (jcbPesquisarEventoColetivo.getSelectedItem().toString().equals("Relato")) {
            listObservacao.addAll(controle.listarRelato(str));//ATUALIZA A LISTA COM OS DADOS DESTA PESQUISA RELEASE
        }
        txtLocalizarObservacao.requestFocus();
        atualizaTabela();//PREENCHE COM OS NOVOS DADOS
    }

    public void vincularCampos() {
        Observacao f = listObservacao.get(jTableObservacao.getSelectedRow());// pega a linha da tabela
        jDateChooserDataObservacao.setDate(f.getDataOcorrido());
        txtRelato.setText(f.getRelato());
        txtAdicionaMatriculaSentenciado.setText(f.getSentenciado().getMatricula());
    }

